// Counter animation for drone specs
function animateCounters() {
    const counters = document.querySelectorAll('.spec-value');
    
    counters.forEach(counter => {
        const target = parseInt(counter.textContent);
        const increment = target / 100;
        let current = 0;
        
        const updateCounter = () => {
            if (current < target) {
                current += increment;
                counter.textContent = Math.ceil(current);
                requestAnimationFrame(updateCounter);
            } else {
                counter.textContent = target;
            }
        };
        
        updateCounter();
    });
}

// Trigger counter animation when solution section is visible
const solutionSection = document.querySelector('.solution');
if (solutionSection) {
    const solutionObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateCounters();
                solutionObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });
    
    solutionObserver.observe(solutionSection);
}

